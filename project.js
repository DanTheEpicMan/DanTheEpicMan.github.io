function onPageLoad() {
    var timelineContainer = document.getElementById('timeline');
    // Use timelineContainer to access and manipulate the container
    
    timelineContainer.style.backgroundColor = 'white';
    timelineContainer.style.height = '100%';
    timelineContainer.style.width = '1px';
    timelineContainer.style.backgroundColor = 'white';
}

window.addEventListener('load', onPageLoad);